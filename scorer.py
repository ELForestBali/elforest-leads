import anthropic
import json
import config

# Клиент Anthropic создаётся один раз при импорте модуля
client = anthropic.AsyncAnthropic(api_key=config.ANTHROPIC_API_KEY)

# Системный промпт — инструкция для Haiku как оценивать лиды
# Это самая важная часть: от качества промпта зависит точность квалификации
SYSTEM_PROMPT = """Ты квалификатор лидов для элитной виллы в Убуде, Бали.
Вилла называется ElForest — бамбуковые виллы, бутик-класс, от $150/ночь.
Целевой клиент: цифровые номады, творческие профессионалы, пары без маленьких детей.

Проанализируй сообщение и верни ТОЛЬКО JSON без markdown-обёртки:
{
  "score": число от 1 до 10,
  "tier": "hot" или "warm" или "cold",
  "intent": "rent" или "buy" или "info" или "other",
  "duration": "short" или "long" или "unknown",
  "budget_signal": true или false,
  "reason": "одна строка — почему такой score"
}

Критерии оценки:

ГОРЯЧИЙ лид (score 8-10, tier: "hot"):
- Явно ищет жильё в Убуде / на Бали прямо сейчас
- Указаны конкретные даты или сроки ("с 15 января", "на 3 месяца")
- Упоминает виллу, дом, жильё с собственным пространством
- Пара, семья, небольшая группа (2-4 человека)
- Сигналы бюджета: "готов платить", называет цифры, спрашивает про премиум
- Ищет тишину, уединение, природу, работу из дома

ТЁПЛЫЙ лид (score 5-7, tier: "warm"):
- Интерес к Убуду / Бали есть, но детали размыты
- Планирует поездку, но даты не определены
- Спрашивает общую информацию о жилье

ХОЛОДНЫЙ (score 1-4, tier: "cold"):
- Просто вопрос без намерения арендовать
- Реклама или спам
- Ищет что-то совсем другое (хостел, отель, апарт)
- Не про Убуд и не про Бали
- Оффтопик

Отвечай ТОЛЬКО валидным JSON. Никаких пояснений, никаких markdown-блоков.
"""

async def score_lead(text: str) -> dict:
    """
    Отправляет текст сообщения в Claude Haiku и получает структурированную оценку.
    
    Haiku — самая быстрая и дешёвая модель Anthropic.
    Стоимость одного запроса: ~$0.0001 (одна десятая цента).
    При 150 запросах в день = ~$0.015/день = ~$0.45/месяц.
    """
    try:
        resp = await client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=200,
            system=SYSTEM_PROMPT,
            messages=[{
                "role": "user",
                # Обрезаем до 1500 символов — длиннее не нужно для квалификации
                "content": text[:1500]
            }]
        )
        return json.loads(resp.content[0].text)
    except json.JSONDecodeError:
        # Если Haiku вернул невалидный JSON (редко, но бывает) — возвращаем нейтральный результат
        return {"score": 0, "tier": "cold", "intent": "other",
                "duration": "unknown", "budget_signal": False,
                "reason": "ошибка парсинга ответа"}
    except Exception as e:
        print(f"[scorer] ошибка: {e}")
        return {"score": 0, "tier": "cold", "intent": "other",
                "duration": "unknown", "budget_signal": False,
                "reason": f"ошибка API: {str(e)}"}
